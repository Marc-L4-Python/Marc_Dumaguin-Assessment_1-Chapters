age = 20


if age < 2:
    print ("\n", "The person is a baby!", "\n")
elif age < 4:
    print ("\n", "The person is a toddler!", "\n")
elif age < 13:
    print ("\n", "The person is a kid!", "\n")
elif age < 20:
    print ("\n", "The person is a teenager!", "\n")
elif age < 65:
    print ("\n", "The person is an adult!", "\n")
else:
    print ("\n", "The person is an elder!", "\n")


# The variable "age" is the basis of the whole code/program. Depending on the value/input of the "age" variable,
# the output of the code will display what "stage of life" the person is at.